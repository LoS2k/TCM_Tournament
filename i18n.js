// ── Fallback: if key missing in lang, use EN ──────────────────────
function t(key, vars = {}) {
  const lang = getLang();
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.en;
  let str = dict[key] ?? TRANSLATIONS.en[key] ?? key;
  Object.entries(vars).forEach(([k, v]) => { str = str.replaceAll(`{${k}}`, v); });
  return str;
}

function getLang() {
  return localStorage.getItem(LANG_KEY) || 'en';
}

function setLang(lang) {
  if (!SUPPORTED_LANGS.includes(lang)) lang = 'en';
  localStorage.setItem(LANG_KEY, lang);
  window.dispatchEvent(new CustomEvent('tc-lang-changed', { detail: lang }));
}

// ── Language picker HTML helper ────────────────────────────────────
function buildLangPicker(containerId) {
  const container = document.getElementById(containerId);
  if (!container) return;

  const current = getLang();
  container.innerHTML = SUPPORTED_LANGS.map(lang => `
    <button
      class="lang-btn${lang === current ? ' active' : ''}"
      onclick="setLang('${lang}'); location.reload();"
      title="${LANG_LABELS[lang]}"
    >${LANG_LABELS[lang]}</button>
  `).join('');
}

// ── Auto-apply data-i18n attributes on page load ──────────────────
// Usage in HTML: <span data-i18n="nav_register"></span>
// or: <input data-i18n-placeholder="reg_team_name_placeholder">
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    el.title = t(key);
  });
}

window.addEventListener('tc-lang-changed', applyTranslations);
